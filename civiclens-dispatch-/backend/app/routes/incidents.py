# ========================================
# CREATE INCIDENT
# ========================================

@router.post("/incidents", response_model=IncidentRead, status_code=201)
async def create_incident(
    incident: IncidentCreate,
    background_tasks: BackgroundTasks,
    db: Database = Depends(get_db)
):
    """
    Create a new incident report.
    
    Steps:
    1. Validate incoming data (automatically done by Pydantic)
    2. Insert incident into database
    3. Schedule background AI processing
    4. Return created incident with ID
    
    Background processing includes:
    - Audio transcription (if audio present)
    - Text classification
    - Risk scoring
    - Summary generation
    """
    
    # Build INSERT query
    query = incidents.insert().values(
        source=incident.source,
        description=incident.description,
        location=incident.location,
        audio_path=None,      # Set later when audio uploaded
        image_path=None,      # Set later when image uploaded
        transcript=None,      # Generated by AI
        summary=None,         # Generated by AI
        risk_score=None,      # Generated by AI
        incident_type=None,   # Generated by AI
        severity=None         # Generated by AI
    )
    
    # Execute query and get auto-generated ID
    incident_id = await db.execute(query)
    
    # Schedule background processing (Day 16 requirement)
    # This runs after the response is sent to the client
    background_tasks.add_task(
        process_incident,
        incident_id=incident_id,
        log_message=f"Processing incident {incident_id}"
    )
    
    # Return the created incident
    return {
        "id": incident_id,
        "source": incident.source,
        "description": incident.description,
        "location": incident.location,
        "audio_path": None,
        "image_path": None,
        "transcript": None,
        "summary": None,
        "risk_score": None,
        "incident_type": None,
        "severity": None
    }


# ========================================
# LIST INCIDENTS (with filtering)
# ========================================

@router.get("/incidents", response_model=list[IncidentRead])
async def list_incidents(
    db: Database = Depends(get_db),
    source: Optional[str] = None,
    incident_type: Optional[str] = None,
    severity: Optional[str] = None,
    min_risk_score: Optional[float] = None,
    limit: int = 10,
    offset: int = 0,
    sort_by: str = "id",
    order: str = "desc"
):
    """
    List incidents with optional filtering and pagination.
    
    Query parameters:
    - source: Filter by incident source (e.g., "citizen")
    - incident_type: Filter by type (e.g., "fire", "medical")
    - severity: Filter by severity (e.g., "high")
    - min_risk_score: Only show incidents with risk >= this value
    - limit: Number of results to return (default 10)
    - offset: Number of results to skip (default 0)
    - sort_by: Field to sort by (default "id")
    - order: Sort order "asc" or "desc" (default "desc")
    """
    
    # Start with base SELECT query
    query = incidents.select()
    
    # Apply filters if provided
    if source:
        query = query.where(incidents.c.source == source)
    
    if incident_type:
        query = query.where(incidents.c.incident_type == incident_type)
    
    if severity:
        query = query.where(incidents.c.severity == severity)
    
    if min_risk_score is not None:
        query = query.where(incidents.c.risk_score >= min_risk_score)
    
    # Apply sorting
    sort_column = getattr(incidents.c, sort_by, incidents.c.id)
    if order.lower() == "desc":
        query = query.order_by(sort_column.desc())
    else:
        query = query.order_by(sort_column.asc())
    
    # Apply pagination
    query = query.limit(limit).offset(offset)
    
    # Execute query and return results
    results = await db.fetch_all(query)
    return results


# ========================================
# GET SINGLE INCIDENT
# ========================================

@router.get("/incidents/{incident_id}", response_model=IncidentRead)
async def get_incident(
    incident_id: int,
    db: Database = Depends(get_db)
):
    """
    Get a specific incident by ID.
    Returns 404 if incident not found.
    """
    
    # Build SELECT query for specific incident
    query = incidents.select().where(incidents.c.id == incident_id)
    
    # Execute query
    incident = await db.fetch_one(query)
    
    # Return 404 if not found
    if incident is None:
        raise HTTPException(
            status_code=404,
            detail=f"Incident {incident_id} not found"
        )
    
    return incident


# ========================================
# UPDATE INCIDENT
# ========================================

@router.patch("/incidents/{incident_id}", response_model=IncidentRead)
async def update_incident(
    incident_id: int,
    incident_update: IncidentUpdate,
    db: Database = Depends(get_db)
):
    """
    Update specific fields of an incident.
    Only provided fields are updated (partial update).
    """
    
    # Check if incident exists
    query = incidents.select().where(incidents.c.id == incident_id)
    existing = await db.fetch_one(query)
    
    if existing is None:
        raise HTTPException(
            status_code=404,
            detail=f"Incident {incident_id} not found"
        )
    
    # Build dictionary of fields to update (exclude None values)
    update_data = incident_update.dict(exclude_unset=True)
    
    if not update_data:
        # Nothing to update
        return existing
    
    # Build UPDATE query
    update_query = (
        incidents
        .update()
        .where(incidents.c.id == incident_id)
        .values(**update_data)
    )
    
    # Execute update
    await db.execute(update_query)
    
    # Fetch and return updated incident
    updated = await db.fetch_one(
        incidents.select().where(incidents.c.id == incident_id)
    )
    return updated


