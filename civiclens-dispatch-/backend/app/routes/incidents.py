# ========================================
# MANUAL PROCESSING TRIGGER
# ========================================

@router.post("/incidents/{incident_id}/process")
async def process_incident_endpoint(
    incident_id: int,
    background_tasks: BackgroundTasks,
    db: Database = Depends(get_db)
):
    """
    Manually trigger AI processing for an incident.
    Useful for reprocessing or if background task failed.
    """
    
    # Check if incident exists
    query = incidents.select().where(incidents.c.id == incident_id)
    incident = await db.fetch_one(query)
    
    if incident is None:
        raise HTTPException(
            status_code=404,
            detail=f"Incident {incident_id} not found"
        )
    
    # Schedule processing
    background_tasks.add_task(
        process_incident,
        incident_id=incident_id,
        log_message=f"Manual processing triggered for incident {incident_id}"
    )
    
    return {
        "status": "processing",
        "message": f"AI processing started for incident {incident_id}",
        "incident_id": incident_id
    }