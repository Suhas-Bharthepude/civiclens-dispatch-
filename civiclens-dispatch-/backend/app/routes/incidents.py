# ========================================

@router.delete("/incidents/{incident_id}")
async def delete_incident(
    incident_id: int,
    db: Database = Depends(get_db)
):
    """
    Delete an incident by ID.
    Returns 404 if incident not found.
    """
    
    # Check if exists
    query = incidents.select().where(incidents.c.id == incident_id)
    existing = await db.fetch_one(query)
    
    if existing is None:
        raise HTTPException(
            status_code=404,
            detail=f"Incident {incident_id} not found"
        )
    
    # Delete the incident
    delete_query = incidents.delete().where(incidents.c.id == incident_id)
    await db.execute(delete_query)
    
    return {
        "message": f"Incident {incident_id} deleted successfully",
        "incident_id": incident_id
    }


# ========================================
# UPLOAD AUDIO FILE
# ========================================

@router.post("/incidents/{incident_id}/audio")
async def upload_audio(
    incident_id: int,
    file: UploadFile = File(...),
    background_tasks: BackgroundTasks = None,
    db: Database = Depends(get_db)
):
    """
    Upload audio file for an incident.
    Saves file to disk and updates incident record.
    Triggers background transcription.
    """
    
    # Check if incident exists
    query = incidents.select().where(incidents.c.id == incident_id)
    incident = await db.fetch_one(query)
    
    if incident is None:
        raise HTTPException(
            status_code=404,
            detail=f"Incident {incident_id} not found"
        )
    
    # Save the uploaded file
    file_path = save_upload_file(file, subfolder="audio")
    
    # Update incident with audio path
    update_query = (
        incidents
        .update()
        .where(incidents.c.id == incident_id)
        .values(audio_path=file_path)
    )
    await db.execute(update_query)
    
    # Trigger reprocessing in background to generate transcript
    if background_tasks:
        background_tasks.add_task(
            process_incident,
            incident_id=incident_id,
            log_message=f"Transcribing audio for incident {incident_id}"
        )
    
    return {
        "message": "Audio uploaded successfully",
        "audio_path": file_path,
        "incident_id": incident_id
    }


